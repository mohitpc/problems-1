        <li>
          <strong>Match Bets Scheduled for 36 Holes or Less</strong>
          <ol>
            <li>If neither player completes one round, all bets open or closed, shall be void. In the event of a player's withdrawal or disqualification after teeing off in the event, a five stroke victory in the match will be awarded to whichever player has progressed further in the tournament, where progress is measured by full rounds completed. Where the bet concerns a match play tournament, the player whose opponent has withdrawn or been disqualified shall be treated as winning by five holes. In the event of both players withdrawing or being disqualified during the same round, all bets open or closed shall be void. Bets on a match involving a player who takes no further part in a tournament following the execution of the bet shall be void, whether open or closed. The maximum make-up in matches scheduled to take place over 36 holes or less is 15 shots. Extra holes (e.g. playoff holes) are irrelevant for the purposes of match bets, unless stated otherwise (see Rule 1.13 under General Sports Rules - Enhanced Match Betting). In an 18- hole match bet, any penalty or disqualification will only affect the settlement of a match bet relating to the round in which it occurs if it is announced before the player tees off in his next round.</li>
