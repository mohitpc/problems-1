          <strong>Limited Overs Matches - Innings Increments </strong>
          <ol>
            <li>A bet may be offered about the number of runs scored in a certain stage of an innings e.g. first 15 overs in a 50 over match or the first 6 overs in a Twenty20 match. If the innings is abandoned prior to the completion of specified number of overs, the market will be settled at the midpoint of the quote at the time at which the innings is reduced.</li>
