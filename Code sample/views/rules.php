          <strong>Other Matches - Innings Runs</strong>
          <ol>
            <li>All bets, open or closed, on a team’s innings runs shall be void if 50 full overs are not bowled unless one team has won, is dismissed or declares prior to that point.</li>
          </ol>
        </li>
        <li>
          <strong>Other Matches - Individual Batsmen Runs/Partnerships</strong>
          <ol>
            <li>All bets, open or closed, on an individual batsman or partnership runs shall be void if 50 full overs are not bowled unless one team has won, is dismissed or declares prior to that point. Bets on partnership totals make up when the next wicket falls. If a batsman in the relevant partnership retires hurt, the partnership is treated as continuing between the remaining batsman and the batsman who comes to the wicket. A partnership is also treated as being ended by the end of an innings.</li>
          </ol>
        </li>
        <li>
          <strong>Other Matches - Session Runs</strong>
          <ol>
            <li>All bets on the market open or closed shall be voided if 20 full overs are not bowled.</li>
          </ol>
