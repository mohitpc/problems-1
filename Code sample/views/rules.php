          <ol>
            <li>1st, 2nd and 3rd innings must all start for bets to stand (i.e. at least one ball must be bowled in the third innings). The only exception to this rule is if a match is abandoned following the team batting 2nd being bowled out (not declared) but before the start of the 3rd innings.</li>
          </ol>
        </li>
        <li>
          <strong>All Matches - Retired Batsmen &amp; Batsmen not Coming to the Crease</strong>
          <ol>
            <li>Where a batsman retires and does not take the field again to bat in the same innings, his score for that innings will be settled at the number of runs he has scored. Batsman runs are often quoted on the assumption that he is coming into bat at that point. Where the batsman does not come out to bat and a price is quoted on a false assumption that he would, all bets during this period on this batsman’s runs will be voided.</li>
          </ol>
        </li>
        <li>
          <strong>All Matches - Markets Not Traded In-play</strong>
          <ol>
            <li>On certain matches we will offer a selection of markets which are not traded in-play. If there is an official result then all bets on these markets will stand. If there is no official result then bets will be void unless that market has already made up. Examples of such markets are wides; multi-wides; total sixes; multi-sixes; total fours; multi-fours etc. Super Overs do not count for these markets.</li>
          </ol>
        </li>
        <li>
          <strong>All Matches – Declaration</strong>
          <ol>
            <li>The act of a declaration does not constitute further action, so bets placed after the last delivery prior to a declaration shall be void pursuant with Rule 1.2.2 under General Sports Rules.</li>
          </ol>
        </li>
        <li>
          <strong>All Matches - Batsmen Match Bets</strong>
          <ol>
            <li>Where a bet consists of a match about the batting performance of two batsmen, both must go in to bat and at least one ball must be bowled (in the relevant innings) after the batsman concerned has gone in to bat (whether to the batsman concerned or not) for any bet, open or closed, to stand.</li>
          </ol>
        </li>
        <li>
          <strong>All Matches – Adjustments</strong>
          <ol>
            <li>Any subsequent official adjustments to the innings total runs or a batsman’s runs after the innings has finished will be included for the purposes of the relevant market. In the event of an adjustment being made to an innings, any bet placed after an adjustment may be re-booked to reflect the fact the bet was placed on an incorrect “so far”.</li>
          </ol>
        </li>
        <li>
          <strong>Official Result (Duckworth-Lewis)</strong>
          <ol>
            <li>The official result, for example for matches determined under the Duckworth-Lewis method or other such mathematical formula, will be determined according to our reasonable opinion, such opinion being formed by reference to all publicly available evidence. The television station which broadcast the underlying event shall be considered the official adjudicator, unless that television station’s opinion differs from all publicly available information in which case <a href="http://www.espncricinfo.com" target="_blank">www.ESPNcricinfo.com</a> shall be considered the official adjudicator and their decision is final.</li>
          </ol>
        </li>
        <li>
          <strong>Completed Match</strong>
          <ol>
            <li>In the event of any match being completed on a reserve day, the makeup of this binary market will be 100. If there is an official result declared for this game, either on the original match day or a reserve day (other than ‘No Result’), the match will be deemed to have been completed and the market settled at 100.</li>
