          <strong>Finishing Positions</strong>
          <ol>
            <li>A player will be deemed to have finished in the exact position that player finished in the field with a maximum make-up of 70. If a player tees off but subsequently withdraws, is disqualified, or retires prior to the cut then that player's finishing position will make up at 70, unless otherwise stated. If a player withdraws, is disqualified or retires after the cut then that player's finishing position makes up at the number of players who made the cut (with a maximum of 70). In the event of a player making the cut and not being allowed to participate in subsequent rounds for whatever reason (including officially making the cut), then that player's finishing position will be settled according to the official result under the ties rule (with a maximum of 70). Bets on a player who fails to tee off in his first round shall be void as will all bets, whether opening or closing, on a player who takes no further part in a tournament following the placing of the bet. If there is a tie, the market will be settled at the average finishing position of the tied players. For example, if 3 players tie for 10th position, the market will make up at 11 ((10+11+12)/3=11). However, if there is a playoff for first place, that playoff will only count for the purposes of determining the winner. No other ties will be split as a result of that playoff, i.e. in a three-way playoff the eventual winner's finishing position will be 1, and those of the other two will be 2.5 ((2+3)/2=2.5), regardless of when they were eliminated in the playoff. Where a market is offered on finishing positions with ‘no maximums’, a player will be deemed to have finished in the exact position that player finished in the field with a maximum make-up equal to the number of players in the field, with the same settlement rules for ties as above. If a player withdraws or is disqualified, that player will be deemed to finish last of those remaining in the tournament at the time of withdrawal, or that pla