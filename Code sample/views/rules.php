        <li>
          <strong>Limited Overs Matches – Binary Bets</strong>
          <ol>
            <li>Bets will be settled on the official result. All bets on a match will be void if the match is officially declared as a ‘no result’. If a match is affected by external factors (such as bad weather) and is not declared as a ‘no result’, bets will be settled based on the official competition rule according to the event’s governing body (this includes matches where the result is determined by the Duckworth-Lewis method, or where the scheduled number of overs is reduced.) The Duckworth Lewis method is the system used to adjust the scores in the event of a delay (such as due to rain) during one-day matches, in order to leave the balance of the match unaltered. The system uses the number of overs each team have still to receive and the number of wickets they have in hand in order to determine a revised target or reach an official result.</li>
            <li>If the result of a one day or Twenty20 match is a tie, and no winner is declared by the official competition rules, then dead heat rules apply and both binaries will settle at 50. If a winner is declared by competition rules, for example a Super Over, most boundaries or previous results, then the winner of this will be deemed the winner of the match. This binary will settle at 100, with the losing team’s binary settling at 0.</li>
          </ol>
        </li>
        <li>
          <strong>Other Matches - Binary Bets</strong>
          <ol>
            <li>In the event of a tied Test Match or County Championship match (i.e. where all innings have been completed and both teams have the same score) the draw will be deemed a loser and settle at 0. Dead heat rules will apply to bets on each team for the outright result, meaning the Team to win Match markets will both settle at 50.</li>
          </ol>
