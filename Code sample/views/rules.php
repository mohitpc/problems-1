            <li>The same principles governing match bets over 36 holes or less will also apply to match bets over more than 36 holes, except that in the event of a player’s withdrawal or disqualification after teeing off in the event, a ten stroke victory in the match will be awarded to whichever player has progressed further in the tournament, where progress is measured by full rounds completed, provided the bet has not already been settled. The maximum make-up in matches scheduled to take place over more than 36 holes is 25 shots. Extra holes (e.g. playoff holes) are irrelevant for the purposes of match bets, unless stated otherwise (see Rule 1.13 under General Sports Rules - Enhanced Match Betting). In the event of either player missing the cut, both players' scores will be doubled at that stage (or adjusted, pro rata, where the number of rounds originally scheduled to be played is not four and/or where the cut takes place in a round other than the second round). Where a match bet has been settled at the cut stage, it is not affected in any way by the subsequent performance of a player who has made the cut, even in the event of his disqualification. For the avoidance of doubt, this rule will also apply to circumstances where the cut is re-defined after the start of the tournament, which may result in an unequal number of rounds being played by participants in a match bet.</li>
          </ol>
